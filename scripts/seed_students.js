function parseStudents(text, section, classTeacher) {
  return text.trim().split('\n').map(line => {
    const parts = line.trim().split(/\s+/);
    const admissionNo = parts[0];
    const name = parts.slice(1).join(' ');
    return {
      admission_no: admissionNo,
      name,
      section,
    };
  }).filter(s => s.admission_no && s.name);
}

async function run() {
  const students10C = parseStudents(class10C, '10C', 'jini');
  const students11A = parseStudents(class11A, '11A', 'ambily krishnan');

  const allStudents = [...students10C, ...students11A];

  console.log('Attempting to insert ' + allStudents.length + ' students...');

  // Insert students
  const { data, error } = await supabase
    .from('students')
    .upsert(allStudents, { onConflict: 'admission_no' })
    .select();

  if (error) {
    console.error('Error inserting students:', error);
  } else {
    console.log('Successfully inserted/updated ' + data.length + ' students!');
  }
}

run();
